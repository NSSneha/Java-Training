import java.util.SortedMap;

public class EscapeSequences {
    public static void main(String[] args) {
        //My name is "Sneha"
        System.out.println("My name is \"Sneha\" "); // '\' is used as an escape sequence
        System.out.println("My name is \\Sneha\\ "); //y name is \Sneha\
        System.out.println("\n"); //escape sequence for new line
        System.out.println("\t\t");//escape sequence for tab space
    }
}
